func randomAccount(owner string) db.Accounts {
	return db.Accounts{
		ID:       int64(util.RandomInt(1, 1000)),
		Owner:    owner,
		Balance:  util.RandomMoney(),
		Currency: util.RandomCurrency(),
	}
}

func requireBodyMatchAccount(t *testing.T, body []byte, account db.Accounts) {
	// data, err := ioutil.ReadAll(body)
	data, err := io.ReadAll(bytes.NewReader(body))
	require.NoError(t, err)

	var gotAccount db.Accounts
	err = json.Unmarshal(data, &gotAccount)
	require.NoError(t, err)
	require.Equal(t, account, gotAccount)
}

func requireBodyMatchAccounts(t *testing.T, body []byte, accounts []db.Accounts) {
	// data, err := ioutil.ReadAll(body)
	data, err := io.ReadAll(bytes.NewReader(body))
	require.NoError(t, err)

	var gotAccounts []db.Accounts
	err = json.Unmarshal(data, &gotAccounts)
	require.NoError(t, err)
	require.Equal(t, accounts, gotAccounts)
}
